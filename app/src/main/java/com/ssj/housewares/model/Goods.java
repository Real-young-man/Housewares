package com.ssj.housewares.model;

public class Goods {
    private String goodsCode;
    private String goodsName;
    private String goodsSpecs;
    private String goodsExecutiveStandard;
    private String goodsManufacturer;
    private String goodsAdd;
    private String goodsTel;
    private String goodsLocal;

    public String getGoodsCode() {
        return goodsCode;
    }

    public void setGoodsCode(String goodsCode) {
        this.goodsCode = goodsCode;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsSpecs() {
        return goodsSpecs;
    }

    public void setGoodsSpecs(String goodsSpecs) {
        this.goodsSpecs = goodsSpecs;
    }

    public String getGoodsExecutiveStandard() {
        return goodsExecutiveStandard;
    }

    public void setGoodsExecutiveStandard(String goodsExecutiveStandard) {
        this.goodsExecutiveStandard = goodsExecutiveStandard;
    }

    public String getGoodsManufacturer() {
        return goodsManufacturer;
    }

    public void setGoodsManufacturer(String goodsManufacturer) {
        this.goodsManufacturer = goodsManufacturer;
    }

    public String getGoodsAdd() {
        return goodsAdd;
    }

    public void setGoodsAdd(String goodsAdd) {
        this.goodsAdd = goodsAdd;
    }

    public String getGoodsTel() {
        return goodsTel;
    }

    public void setGoodsTel(String goodsTel) {
        this.goodsTel = goodsTel;
    }

    public String getGoodsLocal() {
        return goodsLocal;
    }

    public void setGoodsLocal(String goodsLocal) {
        this.goodsLocal = goodsLocal;
    }
}
